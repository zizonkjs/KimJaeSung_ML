# Classification 의 성능평가 지표들을 설명하세요

# True Positive (TP) : 모델이 Positive라고 예측했고, 실제로도 Positive인 경우
# True Negative (TN) : 모델이 Negative라고 예측했으며, 실제로도 Negative인 경우
# False Positive (FP) : 모델이 Positive라고 예측했지만 실제로는 Negative인 경우
# False Negative (FN) : 모델이 Negative라고 예측했지만 실제로는 Positive인 경우

# Accuracy(정확도) : 전체 샘플에서 정확하게 예측한 샘플의 비율
# Accuracy = TP+TN / TP+TN+FP+FN
# 특징 : 데이터가 불균형 할 때는 정확도 만으로는 모델의 성능평가를 할 수 없음.

# Precision (정밀도) : 모델이 양성이라고 예측한 샘플 중에서 실제로 양성인 샘플의 비율을 나타냄
# Precision = TP / TP+FP
# 특징 : False Positive를 줄이는 것이 중요한 경우, 스팸 필터에서 중요한 지표임

# Recall (재현율) : 실제 양성 샘플 중에서 모델이 양성으로 정확하게 예측한 비율
# Recall = TP / TP+FN
# False Negative를 줄일 때 중요한 지표임

# F1-Score : Prescision과 Recall의 조화 평균을 나타내며, 두 지표 간의 균형을 평가
# F1-Score = 2 * (Precision * Recall / (Precision+Recall))
# 특징 : Precision과 Recall의 균형이 중요 할 때 유용함

