# Mean Absolute Error (MAE):
# 설명 : MAE는 예측된 값과 실제 값 간의 절대적 차이의 평균을 계산함
#       예측된 값과 실제 값의 차이의 절대값을 평균화하여 계산함
# 특징 : 모든 오차를 동일하게 취급, 이상치에 덜 민감함

# Mean Squared Error (MSE):
# 설명 : 예측된 값과 실제 값 간의 차이를 제곱한 후 평균을 계산함
#        제곱을 통해 큰 오차에 더 큰 패널티를 부여함
# 특징 : 큰 오차를 강조함, 모델의 오차를 줄이는데 더 집중, 제곱단위라 해석하기 어려움

# Root Mean Squared Error (RMSE):
# 설명 : MSE의 제곱근을 취한 값임. MSE와 유사, 실제 값의 단위와 동일하게 맞추기 위해 사용
# 특징 : MSE의 단점을 보완, 단위가 예측 값과 동일해서 해석이 더 직관적임

# R-suqared (R제곱) or Coefficient of Determination:
# 설명 : R**2은 모델이 데이터를 얼마나 잘 설명하는지 나타내는 지표
#       값은 0~1 사이로 표현됨, 1에 가까울수록 모델이 데이터를 잘 설명함
# 특징 : R**2 값이 높을수록 모델의 설명력이 높지만, 이 값으로 모델의 성능을 판단하기엔 어려움
#       데이터 분산이 작을 경우 낮은 R**2값이 나오더라도 모델이 잘작동할 수 있음.

# Adjusted R-squared:
# 설명 : R**2 값은 설명 변수가 많아질수록 높아질 수 있음.
#       이를 보정하기 위해 Adjusted R**2이 사용되며, 모델에 포함된 변수의 수를 고려해 조정된 값을 제공함
# 특징 : 변수가 많아져도 Adjusted R**2은 변수가 의미 있게 기여할 때만 증가함으로 
#        모델 성능을 평가 할 때 유용함

# Mean Absolute Percentage Error (MAPE):
# 설명 : MAPE는 예측 값과 실제 값 간의 오차를 백분율로 표현한 값의 평균임
# 특징 : MAPE는 직관적으로 이해하기 쉬운 지표이지만, 실제 값이 0에 가까워지면 불안정해짐

# Explained Variance Score:
# 설명 : 예측된 값이 실제 값의 변동성을 얼마나 잘 설명하는가를 평가함
# 특징 : R**2과 유사하지만, 분산을 사용하여 설명된 변동성을 측정함

